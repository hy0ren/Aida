function interpolate(template: string, vars?: Record<string, string | number>) {
  if (!vars) return template;
  return template.replace(/\{\{(\w+)\}\}/g, (_, key: string) => String(vars[key] ?? ""));
}

export function normalizeLocale(language?: string): SupportedLocale {
  if (!language) return "en";
  return languageToLocale[language.trim().toLowerCase()] ?? "en";
}

export function t(
  language: string | undefined,
  key: TranslationKey,
  vars?: Record<string, string | number>,
): string {
  const locale = normalizeLocale(language);
  const override = localeOverrides[locale]?.[key];
  const dict = translations[locale as keyof typeof translations] ?? translations.en;
  const fallback = translations.en[key];
  return interpolate(override ?? (dict as typeof translations.en)[key] ?? fallback, vars);
}

export type I18nKey = TranslationKey;
